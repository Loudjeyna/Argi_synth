    function getLang() { return currentLang; }

    function setLang(lang) {
        if (!translations[lang]) lang = 'en';
        currentLang = lang;
        try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}
        document.documentElement.lang = lang === 'ar' ? 'ar' : 'en';
        document.documentElement.dir = rtlLangs[lang] ? 'rtl' : 'ltr';
        applyTranslations();
        dispatchLangChange();
    }

    function t(key, fallback) {
        var val = translations[currentLang] && translations[currentLang][key];
        if (val !== undefined) return val;
        if (translations.en && translations.en[key] !== undefined) return translations.en[key];
        return fallback || key;
    }

    function applyTranslations(root) {
        root = root || document;
        var els = root.querySelectorAll('[data-i18n]');
        els.forEach(function(el) {
            var key = el.getAttribute('data-i18n');
            var val = t(key);
            if (val !== key) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA' || el.tagName === 'SELECT') {
                    var placeholder = el.getAttribute('data-i18n-placeholder');
                    if (placeholder) el.placeholder = t(placeholder);
                } else {
                    el.textContent = val;
                }
            }
        });

        // data-i18n-placeholder for non-input elements too
        var phs = root.querySelectorAll('[data-i18n-placeholder]');
        phs.forEach(function(el) {
            var key = el.getAttribute('data-i18n-placeholder');
            el.placeholder = t(key);
        });

        // data-i18n-value for input values
        var vals = root.querySelectorAll('[data-i18n-value]');
        vals.forEach(function(el) {
            var key = el.getAttribute('data-i18n-value');
            el.value = t(key);
        });

        // data-i18n-title for title attributes
        var titles = root.querySelectorAll('[data-i18n-title]');
        titles.forEach(function(el) {
            var key = el.getAttribute('data-i18n-title');
            el.title = t(key);
        });
    }

    function translatePage() {
        setLang(currentLang);
    }

    var _changeListeners = [];

    function onLangChange(fn) {
        _changeListeners.push(fn);
    }

    function dispatchLangChange() {
        _changeListeners.forEach(function(fn) { try { fn(currentLang); } catch (e) {} });
    }

    // Initialize
    document.documentElement.lang = currentLang === 'ar' ? 'ar' : 'en';
    document.documentElement.dir = rtlLangs[currentLang] ? 'rtl' : 'ltr';

